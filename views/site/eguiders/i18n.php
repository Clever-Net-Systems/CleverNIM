This section allows you to decide whether your users will be able to use your application in multiple languages.
<ul>
	<li>Choose the fixed language selection mode to force all your users to use the language you will specify in the Fixed / default language dropdown box</li>
	<li>If you choose the "Let the browser decide" language selection mode, the application will try to determine the user's language from the configuration of his browser. If the browser language is in the "Authorized languages" list, that language will be used. Otherwise, the "Default language" will be used.</li>
	<li>If you choose the "Let the user decide" language selection mode, the user will be able to choose his language in his profile, from the list of "Authorized languages"</li>
</ul>

