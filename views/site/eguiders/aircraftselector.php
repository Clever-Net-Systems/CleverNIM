The Aircraft Selector allows you to choose the currently selected aircraft.
<ul>
	<li>The currently selected aircraft appears in the top right corner of the screen.</li>
	<li>The currently selected aircraft is the aircraft on which reporting will be done and for which you can create work orders and other tasks.</li>
	<li>Even if an aircraft is selected, all global reporting is done on all aircrafts for which you have the right to report.</li>
	<li>The list of aircrafts from which you can choose the currently selected aircraft depends on your user role other parameters. Please contact Tempeus if you do not see an aircraft you think you should be able to select.</li>
</ul>

